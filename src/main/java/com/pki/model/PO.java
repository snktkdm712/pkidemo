package com.pki.model;

public class PO {
	
	private String privateKeyFilePath;
	private String publicKeyFilePath;
	private String xmlPayLoadPath;
	private String senderPublicKeyPath;
	
	public String getPrivateKeyFilePath() {
		return privateKeyFilePath;
	}
	public void setPrivateKeyFilePath(String privateKeyFilePath) {
		this.privateKeyFilePath = privateKeyFilePath;
	}
	public String getPublicKeyFilePath() {
		return publicKeyFilePath;
	}
	public void setPublicKeyFilePath(String publicKeyFilePath) {
		this.publicKeyFilePath = publicKeyFilePath;
	}
	public String getXmlPayLoadPath() {
		return xmlPayLoadPath;
	}
	public void setXmlPayLoadPath(String xmlPayLoadPath) {
		this.xmlPayLoadPath = xmlPayLoadPath;
	}
	public String getSenderPublicKeyPath() {
		return senderPublicKeyPath;
	}
	public void setSenderPublicKeyPath(String senderPublicKeyPath) {
		this.senderPublicKeyPath = senderPublicKeyPath;
	}
	
	
	
	
	

}
